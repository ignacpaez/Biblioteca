    private void btnagregarpaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarpaisActionPerformed
        int cod_pais = Integer.parseInt(txtcodpais.getText());
        String nombre = txtnompais.getText();
        int cod_ciu = Integer.parseInt(txtcod_ciu.getText());
        
        Pais pa = new Pais (cod_pais, nombre, cod_ciu);
    
        String msjAgregar = libroBo.agregar(pa);
        listapais.add(pa);
    
        JOptionPane.showMessageDialog(null, msjAgregar);
    }//GEN-LAST:event_btnagregarpaisActionPerformed

