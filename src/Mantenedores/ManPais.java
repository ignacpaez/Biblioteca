    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel Pais= new DefaultTableModel();
        JTable tabla= new JTable(Pais);
        
        Pais.addColumn("Codigo");
        Pais.addColumn("Nombre");
        Pais.addColumn("Codigo Ciudad");

        ResultSet rs= libroBo.listarpa();
       try{
        while(rs.next()){
            Object [] fila= new Object [3];
             for(int i=0;i<3;i++){
                 fila[i]=rs.getObject(i+1);
             }
             Pais.addRow(fila);
        }     
       }catch(Exception ex){        
        }
       jTablaPais.setModel(Pais); 
    }//GEN-LAST:event_jButton4ActionPerformed

