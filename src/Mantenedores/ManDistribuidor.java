    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cod_idi = txtrut.getText();
        String nombre = txtnombre.getText();
        int telefono = Integer.parseInt(txttelefono.getText());
        int cod_imp = Integer.parseInt(txtimprenta.getText());
        int cod_ciu = Integer.parseInt(txtciudad.getText());

        Distribuidor dist = new Distribuidor(cod_idi, nombre, telefono, cod_imp, cod_ciu);

        String msjAgregar = libroBo.agregar(dist);
        listafabr.add(dist);

        JOptionPane.showMessageDialog(null, msjAgregar);
    }//GEN-LAST:event_jButton2ActionPerformed

