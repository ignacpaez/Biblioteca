    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
    
        int nro_serie = Integer.parseInt(txtnroserie.getText());
        int cod_edit = Integer.parseInt(txtcodedit.getText());
        String nombre_aut = txtnombreaut.getText();
        String nombre = txtnombre.getText();
        int cod_gen = Integer.parseInt(txtcodgen.getText());
        int cod_idi = Integer.parseInt(txtidioma.getText());
        int precio = Integer.parseInt(txtprecio.getText());
    
    
        Libro l = new Libro(nro_serie, cod_edit, nombre_aut, nombre, cod_gen, cod_idi, precio);
    
        
        String msjAgregar = libroBo.agregar(l);
        listalibros.add(l);
    
        JOptionPane.showMessageDialog(null, msjAgregar);
    }//GEN-LAST:event_jButtonAgregarActionPerformed

