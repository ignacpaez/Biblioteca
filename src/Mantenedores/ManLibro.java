    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        
        DefaultTableModel Libro= new DefaultTableModel();
        JTable tabla= new JTable(Libro);
        
        Libro.addColumn("Nro Serie");
        Libro.addColumn("Codigo Editorial");
        Libro.addColumn("Autor");
        Libro.addColumn("Nombre Libro");
        Libro.addColumn("Cod Idioma");
        Libro.addColumn("Genero");
        
        
        ResultSet rs= libroBo.listarli();
       try{
        while(rs.next()){
            Object [] fila= new Object [6];
             for(int i=0;i<6;i++){
                 fila[i]=rs.getObject(i+1);
             }
             Libro.addRow(fila);
        }     
       }catch(Exception ex){        
        }
       jTablaLibros.setModel(Libro);                 
    }//GEN-LAST:event_btnListarActionPerformed

