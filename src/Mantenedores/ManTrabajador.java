    private void btnciudadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnciudadesActionPerformed
        DefaultTableModel Ciudad= new DefaultTableModel();
        JTable tabla= new JTable(Ciudad);

        Ciudad.addColumn("Codigo");
        Ciudad.addColumn("Nombre");
        Ciudad.addColumn("Codigo Comuna");

        ResultSet rs= libroBo.listarciu();
        try{
            while(rs.next()){
                Object [] fila= new Object [3];
                for(int i=0;i<3;i++){
                    fila[i]=rs.getObject(i+1);
                }
                Ciudad.addRow(fila);
            }
        }catch(Exception ex){
        }
        jTablaCiudades.setModel(Ciudad);
    }//GEN-LAST:event_btnciudadesActionPerformed

