    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        String rut_tra = txtrut.getText();
        String nombre = txtnombre.getText();
        String ApellidoP = txtapellidop.getText();
        String ApellidoM = txtapellidom.getText();
        String fec_nac = txtfecha.getText();
        int tipo_tel = Integer.parseInt(txttipotel.getText());
        int telefono = Integer.parseInt(txttelefono.getText());
        String correo = txtcorreo.getText();
        int cod_ciu = Integer.parseInt(txtcod_ciu.getText());
        int cod_cont = Integer.parseInt(txtcod_cont.getText());

        Trabajador tra = new Trabajador(rut_tra, nombre, ApellidoP, ApellidoM, fec_nac, tipo_tel, telefono, correo, cod_ciu, cod_cont);

        String msjAgregar = libroBo.agregar(tra);
        listatrabajador.add(tra);

        JOptionPane.showMessageDialog(null, msjAgregar);
    }//GEN-LAST:event_btnregistrarActionPerformed

